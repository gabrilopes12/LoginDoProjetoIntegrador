    private void entrarJbuttonActionPerformed(java.awt.event.ActionEvent evt) {                                              
        //pega o email
        String email = emailField.getText();
        //pega a senha do usuário como char[] e converte para String
        String senha = new String(jPasswordField1.getPassword());
        
        //verifica se o usuário é válido
        
        try{
            Usuario usuario = new Usuario(email, senha); 
            DAO dao = new DAO();
            if(dao.verificaProfessor(usuario)){
                JOptionPane.showMessageDialog (null, "Bem vindo professor!");
                Usuario usuarioP = new Usuario(email);
                TelaProfessor tTP = new TelaProfessor(usuarioP); // tela do professor
                tTP.setVisible(true);
                this.dispose();
            
            }
            else{
            
            if(dao.existe(usuario)){
                JOptionPane.showMessageDialog (null, "Bem vindo!");
                // usar um comando que acessa a base de dados com um select e pega o id do usuario inserido
                Usuario usuarioA = new Usuario(email);
                telaTeste tT = new telaTeste(usuarioA); // tela de incio do jogo 
                tT.setVisible(true);
                this.dispose();
            }
            else{
                JOptionPane.showMessageDialog(null, "Usuário inválido, realize o cadastro!");
                CadastroFrame cf = new CadastroFrame();
                cf.setVisible(true);
                this.dispose();
            }
           }
            
    }                                           
    catch(Exception e){
       JOptionPane.showMessageDialog (null, "Problemas técnicos. Tente novamente mais tarde");
       e.printStackTrace();
    }
    }                                             


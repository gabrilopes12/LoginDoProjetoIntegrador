    public class AlunosPorCursoTableModel extends AbstractTableModel {
        private List <Usuario> alunos;
        private String [] colunas = { "Nome", "Email"};
        public AlunosPorCursoTableModel () throws Exception{
        DAO dao = new DAO ();
        this.alunos = dao.buscaAlunos();
        }
      @Override
           public int getRowCount() {
           return alunos.size();
            }
        @Override
        public int getColumnCount() {
        return 2;
        }
         @Override
           public Object getValueAt(int rowIndex, int columnIndex) {
            switch (columnIndex){
               case 0:
                    return this.alunos.get(rowIndex).pegarnome();
                case 1:
                    return this.alunos.get(rowIndex).pegaremail();
        default:
            return null;
        }
        }
        
        @Override
        public String getColumnName(int column) {
        return this.colunas[column];
 }
 }
    
    
